ZimoType GetZimoData(uint8_t * chr)
{
    uint8_t index;
    for(index=0;index<ZIMO_NUM;index++)
    {
        if(chr[0] ==ZimoIndex[index*3] && chr[1] ==ZimoIndex[index*3+1] && chr[2] ==ZimoIndex[index*3+2] )
        {
            break;
        }
    }
    if(ZIMO_NUM == index)
    {
        return 0;
    }
    return (ZimoType)ZimoData[index];
}
