ZimoType GetZimoData(uint8_t * chr)
{
    uint8_t index;
    if(chr[0] <= 127)
    {
        for(index=0;index<62;index++)
        {
            if(chr[0] == ZimoIndex[index])
            {
                break;
            }
        }
        if(index >= 62)
        {
            return 0;
        }
        else
        {
            return (ZimoType)ZimoData[index];
        }
        
    }
    else
    {
        // 汉字
        for(index=0;index<ZIMO_NUM-62;index++)
        {
            if(chr[0] ==HanziIndex[index*3] && chr[1] ==HanziIndex[index*3+1] && chr[2] ==HanziIndex[index*3+2] )
            {
                break;
            }
        }
        if(index >= ZIMO_NUM-62)
        {
            return 0;
        }
        else
        {
            return (ZimoType)ZimoData[index+62];
        }
        
    }

}
